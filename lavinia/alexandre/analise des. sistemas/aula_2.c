#include <stdio.h>
#include <stdbool.h>
// biblioteca do tipo bool

int main (){

    int x = 10; // var tipo inteiro
    float pi = 3.14; // var do tipo numeros decimais
    char letra = 'A'; // var para caracteres entre aspas simples
    bool verdadeiroefalso = true; // var para declarar variaveis do tipo booleano, true or false

    printf("valor de x: %d\n", x); // %d imprime variaveis do tipo inteiro
    printf("valor de pi: %f\n", pi); // %f imprime variaveis com 2 casas decimais
    printf("letra: %c\n", letra); // %c imorime caracteres entre aspas
    printf("1 verdadeiro e 0 falso: %d\n", verdadeiroefalso); // %d também usado para true or false 1 e 0

    return 0;
}

